public enum ExceptionCode {
    USER_NOT_FOUND("1001"),
    USER_OLD_PASSWORD_ERROR("1002"),
    NEW_PASSWORD_CONFIRM_PASSWORD_DIFF("1003"),
    USER_MODIFY_ERROR("1004");

    private String code;

    private ExceptionCode(String code) {
        this.code = code;
    }

    @Override
    public String toString() {
        return this.code;
    }
}
